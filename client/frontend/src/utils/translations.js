export const t = (key, lang = 'en', vars) => {
  const template = translations[lang]?.[key] || translations.en[key] || key
  if (!vars) return template
  return Object.keys(vars).reduce((acc, k) => {
    return acc.replaceAll(`{${k}}`, String(vars[k]))
  }, template)
}

